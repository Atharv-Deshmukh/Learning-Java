package com.LearningJava.basics;

public class ConditionController {

	public static void main(String[] args) {
		// If Else
		int a = 10;
		int b = 10;
		
		if(a > b) {
			System.out.println("a > b");
		}
		else if(a < b){
			System.out.println("a < b");
		}
		else {
			System.out.println("a = b");
		}

	}

}
